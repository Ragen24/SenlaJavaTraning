package com.senlatask.bookstore.model.enumerations;

/**
 * Order status enumeration
 * PROCESSED — order is processed
 * PACKAGING — order is packaging
 * DELIVERING — order is delivering
 * DONE — order is completed
 * CANCELED — order is canceled
 */
public enum OrderStatus {
    PROCESSED,
    PACKAGING,
    DELIVERING,
    DONE,
    CANCELED
}
